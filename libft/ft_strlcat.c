#include "libft.h"

size_t			ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t		i;
	size_t		j;

	if (ft_strlen(dst) >= size || !dst || !src)
		return (size + ft_strlen(src));
	i = ft_strlen(dst);
	j = 0;
	while (((i + j) < (size - 1)) && src[j])
	{
		dst[i + j] = src[j];
		j++;
	}
	dst[i + j] = '\0';
	return (i + ft_strlen(src));
}
